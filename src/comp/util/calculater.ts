const add = (x: number, y: number) => {
    const result = x+y;
    return result;
}
export {add};

const subtract = (x: number, y: number) => {
    const result = x-y;
    return result;
}

const multiply = (x: number, y: number) => {
    const result = x*y;
    return result;
}

export { subtract, multiply };

export const divide = (x: number, y: number) => {
    const result = x/y;
    return result;
}

