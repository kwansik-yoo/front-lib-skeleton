const add = (x, y) => {
    const result = x + y;
    return result;
};
export { add };
const subtract = (x, y) => {
    const result = x - y;
    return result;
};
const multiply = (x, y) => {
    const result = x * y;
    return result;
};
export { subtract, multiply };
export const divide = (x, y) => {
    const result = x / y;
    return result;
};
